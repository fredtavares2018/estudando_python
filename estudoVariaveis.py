# tipos de variáveis

a = 2
b = 3.4
c = True
d = 8 + 7j
e = 'Teste'

# Para saber os tipos usamos  type

print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))

# resposta do terminal

# <class 'int'> inteiro
# <class 'float'> decimal
# <class 'bool'> boleano
# <class 'complex'> complexo
# <class 'str'> string

# com elas podemos fazer as operações aritméticas 

# x != y        # x é diferente de y
# x > y         # x é maior que y
# x < y         # x é menor que y
# x >= y        # x é maior ou igual a y
# x <= y        # x é menor ou igual a y

print('Soma ', a+b, 'Subtração ', a-b)
print('Multiplicação ', a*b, 'Divisão ', a/b)
print('Potenciação ', b**a)

# resposta do terminal

# Soma  5.4 Subtração  -1.4
# Multiplicação  6.8 Divisão  0.5882352941176471
# Potenciação  11.559999999999999
